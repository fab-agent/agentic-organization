export type TranslationKey = keyof typeof dict.tr;

const STORAGE_KEY = 'fab_locale';
const VALID_LOCALES: Locale[] = ['tr', 'en'];

function loadLocale(): Locale {
  if (typeof localStorage === 'undefined') return 'tr';
  const saved = localStorage.getItem(STORAGE_KEY) as Locale | null;
  return saved && VALID_LOCALES.includes(saved) ? saved : 'tr';
}

let _locale = $state<Locale>(loadLocale());

export const i18n = {
  get locale() { return _locale; },
  set locale(v: Locale) {
    _locale = v;
    if (typeof localStorage !== 'undefined') {
      localStorage.setItem(STORAGE_KEY, v);
    }
  },
  t(key: TranslationKey): string {
    return (dict[_locale] as Record<string, string>)[key] ?? (dict.tr as Record<string, string>)[key] ?? key;
  },
};

export function t(key: TranslationKey): string {
  return i18n.t(key);
}
